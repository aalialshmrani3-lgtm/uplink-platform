def save_to_json(samples: List[Dict[str, Any]], filename: str = "ideas_outcomes_seed_data.json"):
    """Save samples to JSON file"""
    filepath = os.path.join(os.path.dirname(__file__), filename)
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(samples, f, ensure_ascii=False, indent=2)
    print(f"\nüíæ Saved to JSON: {filepath}")
    return filepath


def seed_database(samples: List[Dict[str, Any]]):
    """Seed database with samples"""
    if not DB_AVAILABLE:
        print("\n‚ö†Ô∏è  Database connector not available. Skipping database seeding.")
        return
    
    try:
        db = DatabaseConnector()
        print("\nüîÑ Seeding database...")
        
        success_count = 0
        for sample in samples:
            try:
                db.insert_idea_outcome(sample)
                success_count += 1
                if success_count % 50 == 0:
                    print(f"  Inserted {success_count}/{len(samples)} samples...")
            except Exception as e:
                print(f"  ‚ö†Ô∏è  Failed to insert {sample['idea_id']}: {e}")
        
        print(f"\n‚úÖ Successfully seeded {success_count}/{len(samples)} samples to database!")
        
    except Exception as e:
        print(f"\n‚ùå Database seeding failed: {e}")


